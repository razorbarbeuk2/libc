#include "../libft.h"

int		ft_count_word(char const *w, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (w[i] != '\0')
	{
		while (w[i] == c)
			i++;
		if (w[i] != c && w[i - 1] == c)
			j++;
		i++;
	}
	return (j);
}
